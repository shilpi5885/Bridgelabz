/* Java program to check two strings are equal or not*/
public class EqualStrings {
	    public static void main(String[] args) {

	        String name1 = "Bridgelabz";
	        String name2 = "bridgelabz";

	        if(name1 == name2)
	            System.out.println("Equal");
	        else
	            System.out.println("Not Equal");
	    }

}
